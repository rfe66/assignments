/* https://github.com/kaneru-os/kaneru/blob/master/kernel/lib/strnlen.c */
size_t strnlen(const char *restrict s, size_t n)
{
    size_t i;
    for(i = 0; *s++ && i < n; i++);
    return i;
}

/* https://github.com/kaneru-os/kaneru/blob/master/kernel/lib/strncat_k.c */
char *strncat_k(char *restrict s1, const char *restrict s2, size_t n)
{
    size_t nc;
    char *save = s1;
    while(*s1 && n--)
        s1++;
    nc = strnlen(s2, --n);
    s1[nc] = 0;
    memcpy(s1, s2, nc);
    return save;
}


/* IBM 8x16 VGA font */
static struct bfont IBM_8x16 = {
    .little_endian = false,
    .char_width = 8,
    .char_height = 16,
    .ascii_offset = 0,
    .data = IBM_8x16_bitmap,
};

static void uart_printf(const char *fmt, ...)
{
    __va_list ap;
    static char buffer[512] = { 0 };
    va_start(ap, fmt);
    vsnprintf(buffer, sizeof(buffer), fmt, ap);
    va_end(ap);
    uart_write(buffer, strlen(buffer));
}

static void lcd_bfont_printf(const struct bfont *font, uint16_t fg, uint16_t bg, size_t cx, size_t cy, const char *fmt, ...)
{
    __va_list ap;
    static char buffer[512] = { 0 };
    va_start(ap, fmt);
    vsnprintf(buffer, sizeof(buffer), fmt, ap);
    va_end(ap);
    lcd_bfont_string(font, fg, bg, cx, cy, buffer);
}

static char buffer[128] = { 0 };
static const char prompt[] = "@ ";
static uint16_t bgcol = 0x0000;
static uint16_t fgcol = 0xFFFF;

static void command(void)
{
    unsigned short color;

    if(sscanf(buffer, "setbg %hx", &color) == 1) {
        bgcol = color;
        return;
    }
    
    if(sscanf(buffer, "setfg %hx", &color) == 1) {
        fgcol = color;
        return;
    }
    
}

static void append(const char *data)
{
    char *cr;
    strncat_k(buffer, data, sizeof(buffer));
    if((cr = strchr(buffer, '\r')) == NULL)
        return;
    *cr = 0;
    uart_printf("\r\n");
    command();
    uart_printf("%s", prompt);
    memset(buffer, 0, sizeof(buffer));
}

int __attribute__((noreturn)) main(void)
{
    char buf[16] = { 0 };
    size_t n;
    
    SystemInit();
    lcd_init();
    
    uart_init(CONFIG_UART_SPEED, CONFIG_UART_WORDSIZE, CONFIG_UART_PARITY, CONFIG_UART_DSTOP);
    uart_printf("\r\n");
    uart_printf("speed=%zu\r\n", (size_t)CONFIG_UART_SPEED);
    uart_printf("wordsize=%zu\r\n", (size_t)CONFIG_UART_WORDSIZE);
    uart_printf("parity=%zu\r\n", (size_t)CONFIG_UART_PARITY);
    uart_printf("stopbits=%d\r\n", CONFIG_UART_DSTOP ? 2 : 1);
    uart_printf("\r\n%s", prompt);
    
    lcd_clear(0x0000);
    lcd_bfont_printf(&IBM_8x16, 0xFFFF, 0x0000, 0, 0, "speed=%zu", (size_t)CONFIG_UART_SPEED);
    lcd_bfont_printf(&IBM_8x16, 0xFFFF, 0x0000, 0, 1, "wordsize=%zu", (size_t)CONFIG_UART_WORDSIZE);
    lcd_bfont_printf(&IBM_8x16, 0xFFFF, 0x0000, 0, 2, "parity=%zu", (size_t)CONFIG_UART_PARITY);
    lcd_bfont_printf(&IBM_8x16, 0xFFFF, 0x0000, 0, 3, "stopbits=%d", CONFIG_UART_DSTOP ? 2 : 1);
    
    for(;;) {
        if((n = uart_read(buf, sizeof(buf))) != 0) {
            uart_write(buf, n);
            append(buf);
        }
    }
}
