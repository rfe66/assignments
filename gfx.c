int __attribute__((noreturn)) main(void)
{
    SystemInit();
    init_display();

    /* Setup screen redraw callbacks */
    memset(screens, 0, sizeof(screens));
    screens[SCREEN_MENU] = &draw_menu;
    screens[SCREEN_TEXT] = &draw_text;
    screens[SCREEN_FIGURE] = &draw_figure;
    screens[SCREEN_BITMAP] = &draw_bitmap;

    /* Set P1.XX (joystick) for INPUT */
    LPC_GPIO1->FIODIR &= ~JOY_UP;
    LPC_GPIO1->FIODIR &= ~JOY_RT;
    LPC_GPIO1->FIODIR &= ~JOY_DN;
    LPC_GPIO1->FIODIR &= ~JOY_LF;
    
    /* Start at MENU */
    set_screen(SCREEN_MENU);

    joystick.cs = 0;
    joystick.ps = 0;
    for(;; ticks++) {
        joystick.cs = LPC_GPIO1->FIOPIN;
        if(screens[current_screen])
            screens[current_screen]();
        joystick.ps = joystick.cs;
    }
}
