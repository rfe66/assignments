size_t strnlen_k(const char *s, size_t n)
{
    size_t i;
    for(i = 0; *s++ && i < n; i++);
    return i;
}

char *strncat_k(char *s1, const char *s2, size_t n)
{
    size_t nc;
    char *save = s1;
    while(*s1 && n--)
        s1++;
    nc = strnlen_k(s2, --n);
    s1[nc] = 0;
    memcpy(s1, s2, nc);
    return save;
}

